if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $name = htmlspecialchars($_POST['name']);
    $email = htmlspecialchars($_POST['email']);
    $subject = htmlspecialchars($_POST['subject']);
    $message = htmlspecialchars($_POST['message']);

    $mail = new PHPMailer(true);

    try {
        // Server settings
        $mail->isSMTP();
        $mail->Host       = 'smtp.gmail.com'; // Your SMTP server (Gmail, Outlook, etc.)
        $mail->SMTPAuth   = true;
        $mail->Username   = 'ariyomiracle1234@gmail.com'; // Your email
        $mail->Password   = 'bius czkl vvfe bghm'; // Your email password (use App Password if using Gmail)
        $mail->SMTPSecure = PHPMailer::ENCRYPTION_STARTTLS; // TLS encryption
        $mail->Port       = 587; // SMTP port

        // Recipients
        $mail->setFrom($email, $name);
        $mail->addAddress('emmaariyom1@gmail.com'); // Your receiving email

        // Content
        $mail->isHTML(true);
        $mail->Subject = $subject;
        $mail->Body    = "<p><strong>Name:</strong> $name</p>
                          <p><strong>Email:</strong> $email</p>
                          <p><strong>Message:</strong><br>$message</p>";

        $mail->send();
        echo 'Your message has been sent!';
    } catch (Exception $e) {
        echo "Error: {$mail->ErrorInfo}";
    }
}
